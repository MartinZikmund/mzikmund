namespace MZikmund.Web.Helpers.Constants;

public static class LayoutSections
{
	/// <summary>
	/// Content belonging to the <head> element.
	/// </summary>
	public const string Head = nameof(Head);

	/// <summary>
	/// Main content of the page.
	/// </summary>
	public const string Body = nameof(Body);

	/// <summary>
	/// Content that should be placed at the end of the <body> element.
	/// </summary>
	public const string BodyEnd = nameof(BodyEnd);

	/// <summary>
	/// Content that should be placed just above main content.
	/// </summary>
	public const string AboveMain = nameof(AboveMain);
}
